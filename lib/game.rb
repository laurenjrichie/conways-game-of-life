class GameOfLife
  attr_reader :grid

  def initialize
    @grid = [
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
      ["-", "-", "-", "-", "-", "-", "-", "-", "-", "-",],
    ]
  end

  def seed(live_cells)
    grid = @grid
    while grid.flatten.count("O") < live_cells
      row = rand(0..9)
      grid[row][0] = "O"
      grid[row] = grid[row].sort_by {rand}
    end
    grid
  end

end
